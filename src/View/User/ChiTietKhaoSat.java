    private void btnTKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTKActionPerformed
       
       if(rdTKTenBaiKhaoSat.isSelected()){
           String key = txtTkTenBKS.getText();
           if(key.isEmpty()){
               JOptionPane.showMessageDialog(null, "Vui lòng nhập tên bài khảo sát tìm kiếm","Cảnh báo",JOptionPane.WARNING_MESSAGE);
           }else{
                List<CauHoi> list = qndao.chiTietKhaoSat(idUser);
                List<CauHoi> listResult = searchTenBKS(list, key);
                tableModel.setRowCount(0);
                showTimKiemChiTietKhaoSat(listResult);
           }
       }
       else{
           JOptionPane.showMessageDialog(null, "Vui lòng lựa chọn hình thức tìm kiếm","Cảnh báo",JOptionPane.WARNING_MESSAGE);
       }
    }//GEN-LAST:event_btnTKActionPerformed


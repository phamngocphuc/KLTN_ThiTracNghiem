     private List<CauHoi> searchTenBKS(List<CauHoi> list, String key){
        List<CauHoi> resultList = new ArrayList<>();
        var regex = ".*" + key + ".*";
        Pattern pattern = Pattern.compile(regex, Pattern.CASE_INSENSITIVE);
        Matcher matcher;
        for (CauHoi cauHoi : list) {
            matcher = pattern.matcher(cauHoi.getTenBKS());
            if (matcher.matches()) {
                resultList.add(cauHoi);
            }
        }
        return resultList;
    }
    
    private void showChiTietKhaoSat(){
        THeaderTable();
        tableModel.setRowCount(0);
        List<CauHoi> list = qndao.chiTietKhaoSat(idUser);
        
        LocalDate date = LocalDate.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyyy");
        String dateCurrent = date.format(formatter);
        
        if(list != null){
            for (CauHoi cauHoi : list) {
            Object[] row = new Object[]{
                cauHoi.getTenBKS(),dateCurrent,cauHoi.getNoiDung(), cauHoi.getLuaChon1(), cauHoi.getLuaChon2(), cauHoi.getLuaChon3(), cauHoi.getLuaChon3(), cauHoi.getHinhThuc(), cauHoi.getTraloi()
            };
                tableModel.addRow(row);
            }
        }
    }
    
